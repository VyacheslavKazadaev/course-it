
def main() -> None:
    # Запрашиваем имя у пользователя
    name = input("Как вас зовут? ")

    # Приветствуем пользователя
    print(f"Привет, {name}! Рады видеть вас!")

    # Дополнительная функция - спросить возраст
    try:
        age = int(input("Сколько тебе лет? "))
        if age < 18:
            print("Ты начинаешь рано - это отлично!")
        else:
            print("Никогда не поздно начать программировать!")
    except ValueError:
        print("Некорректный возраст, продолжаем без него")

if __name__ == "__main__":
    main()